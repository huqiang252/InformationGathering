import math

print(math.pi)  # π
print(math.tau) # 2π
print(math.pow(2, 10))  # 乘方函数
print(math.sqrt(4)) # 开平方函数
print(math.ceil(3.14))  # 将数字向上舍入到最接近的整数，比当前参数大的最小整数
print(math.floor(3.14)) # 将数字向下舍入到最接近的整数，比当前参数小的最大整数
